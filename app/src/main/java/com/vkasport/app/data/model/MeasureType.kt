package com.vkasport.app.data.model

/**
 * Как считается упражнение. Определяет, какие поля вводятся в подходе
 * и как отображается результат.
 *
 * ВАЖНО: имена констант (name) хранятся в БД и в бэкапах как строки —
 * НЕ переименовывать существующие! Новые типы добавлять можно свободно.
 */
enum class MeasureType(
    val title: String,       // название в выпадающем меню «как считать»
    val shortHint: String    // короткая подсказка для полей ввода
) {
    /** Классика: вес × повторы. Вес 0 = «без доп. веса» (отжимания и т.п.) */
    WEIGHT_REPS("Вес × повторы", "кг × повт."),

    /** Только количество повторений (без веса вообще) */
    REPS("Только повторы", "повт."),

    /** Время удержания/выполнения: планка, растяжка, вис */
    TIME("Время", "мин:сек"),

    /** Дистанция + время: бег, ходьба */
    DISTANCE("Дистанция", "км · мин:сек"),

    /** Кардио-тренажёр: нагрузка, скорость, время (этап «жиросжигание») */
    CARDIO("Кардио-тренажёр", "нагрузка · км/ч · мин:сек")
}